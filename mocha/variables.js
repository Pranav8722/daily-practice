// variables in javascript 
// 1. var
// 2. let
// 3. const

// 1)var 
var a=10; //declaration 👍🏻
a=20;   //reinitialization 👍   🏻
var a=30; //re declaration 👍   🏻
console.log(a); 

// 2)let
let b=10; //declaration 👍  🏻
b=20; //reinitialization 👍  🏻
// let b=30; //re declaration not allowed ❌
console.log(b); //output=20


// 3)const  
const c=10; //declaration 👍  🏻
// c=20; //reinitialization not allowed ❌
// const c=30; //re declaration not allowed ❌  
console.log(c); //output=10
